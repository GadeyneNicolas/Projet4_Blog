Finalités du traitement <br>
Les données personnelles collectées sont utilisées pour la gestion et l'expédition des commandes, la gestion de la clientèle et du support client, la gestion des commentaires du blog et des évaluations de produits, l'affichage de propositions d'achats complémentaires de produits, la connexion aux espaces privés, l'envoi d'emails de suivi de commandes, l'envoi d'emails de réponses aux formulaires de contact, l'envoi par emails d'offres promotionnelles liées aux points de fidélité, l'envoi par emails de lettres d'informations.
<br>
Certaines données sont collectées automatiquement du fait de vos actions sur le site afin d'effectuer des mesures d'audience (le nombre de pages vues, le nombre de visites du site, les produits consultés, etc.) ou sont nécessaires à la prévention et la résolution d’incidents techniques.
</p>
<h3>Données collectées</h3>
<p>
Les données personnelles collectées et traitées sont notamment vos nom, prénom, civilité, email, mot de passe, numéro de téléphone, adresse IP, adresse postale, avatar, pseudo, site internet, historique de commandes, données de connexions.
<br>
Aucune donnée banquaire n'est concervée en dehors de l'historique de paiement (référence de la commande et statut du paiement uniquement), nécessaire au traitement des litiges et à la prévention et la résolution d’incidents techniques.
<br>
Nous nous engageons à ne collecter que le minimum de données nécessaires au bon fonctionnement du service fourni par ce site internet. Le caractère obligatoire ou facultatif des données collectées vous est signalé au moment de leur saisie par une mention "Obligatoire" (ou tout autre indicateur visuel approrié comme un astérisque ou un élément en gras) ou "Facultatif".
</p>
<h3>Durée de conservation</h3>
<p>
Les données sont conservées pendant toute la durée d'utilisation du service puis sont archivées pour une durée supplémentaire en lien avec les durées de prescription et de conservation légale pour des raisons strictement limitées et autorisées par la loi (paiement, garantie, litiges, etc.). Passé ce délai, elles sont supprimées.
</p>
